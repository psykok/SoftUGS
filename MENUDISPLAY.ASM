; == RÈglage de la luminositÈ de l'afficheur                ==
; ==                                                        ==
; ==   - Encodeur -> Changer la valeur                      ==
; ==   - Menu     -> Valider la nouvelle valeur             ==
; ==   - StandBy  -> Remonter au menu prÈcÈdent sans sauver ==
