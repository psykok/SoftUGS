; == Cette valeur est contenue dans MenuReg1                 ==
; =============================================================

AfficheIdleTimeOut:

		ldi		Work,0x40							; DÈbut de seconde ligne
		call	DisplayPlaceCurseur
		ldi		ZH,HIGH(MenuDisplayTOValueMessage*2)
		ldi		ZL,LOW(MenuDisplayTOValueMessage*2)
		call	DisplayAfficheChaine
		call	DisplayArrow

		mov		Work1,MenuReg1						; Dans un registre immÈdiat

		cpi		Work1,0								; Pour '0', on affiche "Never"							
		breq	TimeOutNever

		cpi		Work1,240							; pour 240, afficher '1 hour'
		breq	TimeOutOneHour

		cpi		Work1,5								; Moins de 2 minutes 
		brlo	TimeOutOneOrLess

		ldi		Work,0x45							; Curseur en position pour les dizaines de minutes
		cpi		Work1,40							; c'est bien plus de 5 minutes ?
		brsh	TimeOutLocCursor					; Vi alors Go

		ldi		Work,0x46							; Sinon curseur pour les unitÈs

TimeOutLocCursor:
		call	DisplayPlaceCurseur

; Comme "l'unitÈ" est de 15 secondes, on divise la valeur par 4 pour avoir le temps en minutes...

		lsr		Work1								; Divise par 2
		lsr		Work1								; Et encore par deux

		mov		LSDVol,Work1						; on transfËre dans le registre de 'conversion'
		call	BinaireToBCD						; et on convertit en dÈcimal

		ldi		Work,48								; Y-a-t-il un 0 au dÈbut (48 en Ascii)
		cp		Work,MSDVol						
		breq	TimeOutAffUnits						; Oui -> On n'affiche que les unitÈs
		
		mov		Char,MSDVol							; Sinon, on affiche le chiffre
		call	DisplayWriteChar

TimeOutAffUnits:									; On affiche les unitÈs des minutes...
		mov		Char,LSDVol
		call	DisplayWriteChar						
		call	AfficheToMinutes		
		ret											; et oual‡

TimeOutNever:										; On Ècrit "Off"
		ldi		Work,0x48
		call	DisplayPlaceCurseur
		call	AfficheToNever
		ret	

TimeOutOneHour:										; On Ècrit '1 hour'
		ldi		Work,0x47
		call	DisplayPlaceCurseur
		ldi 	Char,'1'
		call	DisplayWriteChar
		ldi 	Char,32
		call	DisplayWriteChar
		ldi 	Char,'H'
		call	DisplayWriteChar
		ldi 	Char,'o'
		call	DisplayWriteChar
		ldi 	Char,'u'
		call	DisplayWriteChar
		ldi 	Char,'r'
		call	DisplayWriteChar
		
		ret
			
TimeOutOneOrLess:
		ldi		Work,0x46
		call	DisplayPlaceCurseur

		cpi		Work1,4								; Une minute ?
		brne	TimeOut30Seconds

		ldi		Char,'1'							; Et on affiche 'Minute'
		call	DisplayWriteChar
		ldi		Char,32
		call	DisplayWriteChar						
		ldi		Char,'M'							
		call	DisplayWriteChar						
		ldi		Char,'i'
		call	DisplayWriteChar						
		ldi		Char,'n'
		call	DisplayWriteChar						
		ldi		Char,'u'
		call	DisplayWriteChar						
		ldi		Char,'t'
		call	DisplayWriteChar						
		ldi		Char,'e'
		call	DisplayWriteChar						

		ret

TimeOut30Seconds:									; On affiche "30 Seconds"

		ldi		Work,0x45
		call	DisplayPlaceCurseur
		
		cpi		Work1,2								; 15 Secondes ?
		brne	TimeOut15Seconds

		ldi		Char,'3'
		call	DisplayWriteChar
		ldi		Char,'0'
		call	DisplayWriteChar
		ldi		Char,32
		call	DisplayWriteChar						
		ldi		Char,'S'							
		call	DisplayWriteChar						
		ldi		Char,'e'
		call	DisplayWriteChar						
		ldi		Char,'c'
		call	DisplayWriteChar						
		ldi		Char,'o'
		call	DisplayWriteChar						
		ldi		Char,'n'
		call	DisplayWriteChar						
		ldi		Char,'d'
		call	DisplayWriteChar						
		ldi		Char,'s'
		call	DisplayWriteChar						

		ret

TimeOut15Seconds:									; On affiche "15 Seconds"

		ldi		Work,0x45
		call	DisplayPlaceCurseur
		
		ldi		Char,'1'
		call	DisplayWriteChar
		ldi		Char,'5'
		call	DisplayWriteChar
		ldi		Char,32
		call	DisplayWriteChar						
		ldi		Char,'S'							
		call	DisplayWriteChar						
		ldi		Char,'e'
		call	DisplayWriteChar						
		ldi		Char,'c'
		call	DisplayWriteChar						
		ldi		Char,'o'
		call	DisplayWriteChar						
		ldi		Char,'n'
		call	DisplayWriteChar						
		ldi		Char,'d'
		call	DisplayWriteChar						
		ldi		Char,'s'
		call	DisplayWriteChar						

		ret

AfficheTOMinutes:
		ldi		Char,32
		call	DisplayWriteChar						
		ldi		Char,'M'							; Et on affiche 'Minutes'
		call	DisplayWriteChar						
		ldi		Char,'i'
		call	DisplayWriteChar						
		ldi		Char,'n'
		call	DisplayWriteChar						
		ldi		Char,'u'
		call	DisplayWriteChar						
		ldi		Char,'t'
		call	DisplayWriteChar						
		ldi		Char,'e'
		call	DisplayWriteChar						
		ldi		Char,'s'
		call	DisplayWriteChar						
				 
		ret

AfficheToNever:										; "Never" pour le time out dÈsactivÈ
		ldi		Char,'N'
		call	DisplayWriteChar						
		ldi		Char,'e'
		call	DisplayWriteChar						
		ldi		Char,'v'
		call	DisplayWriteChar						
		ldi		Char,'e'
		call	DisplayWriteChar						
		ldi		Char,'r'
		call	DisplayWriteChar						

		ret
